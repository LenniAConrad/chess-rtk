package chess.book.render;

/**
 * Stores a horizontal text block.
 *
 * @param x left edge
 * @param y top edge
 * @param width wrap width
 * @since 2026
 * @author Lennart A. Conrad
 */
record TextBlock(
	/**
	 * Stores the x.
	 */
	double x,
	/**
	 * Stores the y.
	 */
	double y,
	/**
	 * Stores the width.
	 */
	double width
) {
}
